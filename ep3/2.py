# 给定字符串
s = "www.moe.gov.cn"

# (1) 输出第一个字符
print("第一个字符:", s[0])

# (2) 输出前三个字符
print("前三个字符:", s[:3])

# (3) 输出后三个字符
print("后三个字符:", s[-3:])

# (4) 输出字符串的总长度
print("字符串的总长度:", len(s))

# (5) 输出字符'o'在字符串中第一个位置的索引值
print("字符'o'在字符串中第一个位置的索引值:", s.index('o'))

# (6) 输出字符'o'出现的总次数
print("字符'o'出现的总次数:", s.count('o'))

# (7) 将字符串中所有的“.”替换为“-”并输出
print("替换后的字符串:", s.replace('.', '-'))

# (8) 将字符串中所有的字母全部转换为大写字母并输出
print("转换为大写字母的字符串:", s.upper())

# (9) 删除字符串中的标点符号，把字符串拆分为四个字符串
split_strings = s.replace('.', ' ').split()
print("拆分后的字符串:", split_strings)

# 再次输出原字符串，观察是否有变化
print("原字符串:", s)